    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        int price = Integer.parseInt(rate_input.getText());
        int quantity = Integer.parseInt(quantity_input.getText());
        int total = price * quantity;
        DefaultTableModel model = (DefaultTableModel) billing_table.getModel();
        model.addRow(new Object[]{pname_input.getText(), description_input.getText(), price, quantity, total});
        finalTotal = finalTotal + total;
        String finalTotal1 = String.valueOf(finalTotal);
        total_input.setText(finalTotal1);

    }//GEN-LAST:event_addActionPerformed

