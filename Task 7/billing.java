    private void name_inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_name_inputActionPerformed
        // TODO add your handling code here:
        String name = name_input.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from buyer where name like '" + name + "%'");
            if (rs.next()) {
                name_input.setText(rs.getString(1));
                contact_input.setText(rs.getString(2));
                email_input.setText(rs.getString(3));
                address_input.setText(rs.getString(4));
            } else {
                contact_input.setText("");
                email_input.setText("");
                address_input.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_name_inputActionPerformed

