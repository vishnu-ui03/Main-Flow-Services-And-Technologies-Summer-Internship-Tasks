    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String name = name_input.getText();
        String contactNo = contact_input.getText();
        String email = email_input.getText();
        String address = address_input.getText();

        String path = "C:\\Users\\hinab\\OneDrive\\Desktop\\My Docs\\Internships\\Main Flow Services and Technologies\\PROJECTS\\Billing Management System\\Bill_pdf\\";
        String fileName = name + " " + datei.getText().replaceAll("[^a-zA-Z0-9\\s]", "_") + ".pdf";
        com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path + fileName));
            doc.open();

            Paragraph paragraph1 = new Paragraph("                                       BTech Days(Billing Management system)\n                                       Contact Number:(+91)7201059632\n\n");
            doc.add(paragraph1);

            Paragraph paragraph2 = new Paragraph("Date: " + datei.getText() + "\nTime: " + timei.getText()
                    + "\n\nBuyer Details :\nName: " + name
                    + "\nContact No.: " + contactNo
                    + "\nE-mail: " + email
                    + "\nAddress: " + address + "\n\n");
            doc.add(paragraph2);

            PdfPTable tbl = new PdfPTable(5);
            tbl.addCell("Name");
            tbl.addCell("Description");
            tbl.addCell("Rate");
            tbl.addCell("Quantity");
            tbl.addCell("Sub Total");
            for (int i = 0; i < billing_table.getRowCount(); i++) {
                String n = billing_table.getValueAt(i, 0).toString();
                String d = billing_table.getValueAt(i, 1).toString();
                String r = billing_table.getValueAt(i, 2).toString();
                String q = billing_table.getValueAt(i, 3).toString();
                String s = billing_table.getValueAt(i, 4).toString();
                tbl.addCell(n);
                tbl.addCell(d);
                tbl.addCell(r);
                tbl.addCell(q);
                tbl.addCell(s);
            }
            doc.add(tbl);

            Paragraph paragraph3 = new Paragraph("\nBilling Details:\nTotal: " + total_input.getText()
                    + "\nPaid Amount: " + paid_input.getText()
                    + "\nReturn Amount: " + return_input.getText()
                    + "\n\nThanks You for Visiting! Please Come Again.\nBtech Days");
            doc.add(paragraph3);

            JOptionPane.showMessageDialog(null, "Bill Generated Succcessfully!");
            setVisible(false);
            new billing().setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
        doc.close();
    }//GEN-LAST:event_saveActionPerformed

